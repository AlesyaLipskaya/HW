package w12;

public class Student2 {
    private String group;
    private String lastName;
    private int age;

    public Student2(String group, String lastName, int age) {
        this.group = group;
        this.lastName = lastName;
        this.age = age;
    }

    public String getGroup() {
        return group;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }
}

